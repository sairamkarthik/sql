CREATE TABLE RANDOM
(
    last_name   VARCHAR(25) NOT NULL,
    first_name  VARCHAR(15) NOT NULL,
    suffix      VARCHAR(5) NULL,
    sex         VARCHAR(1) NULL,
    city        VARCHAR(20) NOT NULL,
    state       VARCHAR(2) NOT NULL,
    country VARCHAR(15) NOT NULL,
    age     int,
    food VARCHAR(25) null,
    hobby Varchar(10)
)